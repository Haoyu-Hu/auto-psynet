#!/usr/bin/env bash
# apsy-scaffold — lay down a PsyNet experiment from a paradigm template, then run psynet update-scripts.
# Usage: apsy-scaffold.sh <target_dir> [paradigm]   (paradigm default: static)
# The template ships {{PLACEHOLDERS}} for implement-paradigm / wire-timeline to fill.
set -uo pipefail
DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
. "$DIR/apsy-common.sh"
ROOT="$(cd "$DIR/.." && pwd)"

target="${1:-$PWD}"
paradigm="${2:-static}"
mkdir -p "$target"

tmpl="$ROOT/config/templates/experiment_${paradigm}.py.tmpl"
if [[ -f "$tmpl" ]]; then
  if [[ -f "$target/experiment.py" ]]; then
    echo "• experiment.py already exists — left untouched"
  else
    cp "$tmpl" "$target/experiment.py"
    echo "• wrote experiment.py (paradigm: $paradigm) — fill the {{PLACEHOLDERS}}"
  fi
else
  echo "⚠️  no template for paradigm '$paradigm'; implement-paradigm must author experiment.py from skills/psynet/psynet-function/${paradigm}.md"
fi

[[ -f "$target/config.txt" ]]       || { cp "$ROOT/config/templates/config.txt.tmpl"       "$target/config.txt";       echo "• wrote config.txt"; }
[[ -f "$target/requirements.txt" ]] || { cp "$ROOT/config/templates/requirements.txt.tmpl" "$target/requirements.txt"; echo "• wrote requirements.txt"; }

# Boilerplate (Dockerfile, docker/, test.py, pytest.ini, docs/) is generated by PsyNet itself.
if command -v psynet >/dev/null 2>&1; then
  if ( cd "$target" && psynet update-scripts ); then
    echo "• ran psynet update-scripts (boilerplate generated)"
  else
    echo "⚠️  psynet update-scripts failed — inspect $target"
  fi
else
  echo "⚠️  psynet not installed; skipped update-scripts. Install psynet (see /apsy:doctor), then run:"
  echo "      ( cd $target && psynet update-scripts )"
fi

echo "scaffold complete: $target"
