#!/usr/bin/env bash
# apsy-repro — assemble an OSF-ready reproducibility package from an experiment.
# Includes: experiment source, analysis scripts + results, the locked preregistration, the decision/
# iteration logs, and a manifest. Does NOT auto-bundle raw participant data — only the ANONYMIZED export
# (privacy; config/ethics-policy.md §1.6).
# Usage: apsy-repro.sh [experiment_dir] [out_dir] [anonymized_data_dir]
set -uo pipefail
DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
. "$DIR/apsy-common.sh"
target="${1:-$PWD}"

label="$(python3 - "$target/.apsy/state.json" <<'PY'
import json, sys
try:
    print(json.load(open(sys.argv[1])).get("label") or "experiment")
except Exception:
    print("experiment")
PY
)"
[[ -n "$label" ]] || label="experiment"
out="${2:-$target/repro-$label}"
anon="${3:-}"

mkdir -p "$out/code" "$out/analysis" "$out/preregistration" "$out/docs" "$out/data"

# --- Code (experiment source; skip pilot artifacts) ---
for f in experiment.py requirements.txt config.txt constraints.txt; do
  [[ -f "$target/$f" ]] && cp "$target/$f" "$out/code/" || true
done
for f in "$target"/*.py; do
  [[ -f "$f" ]] || continue
  case "$(basename "$f")" in apsy_llm_participant.py|conftest.py|experiment.py) continue;; esac
  cp "$f" "$out/code/" || true
done

# --- Analysis + preregistration + decision trail ---
[[ -d "$target/.apsy/analysis" ]] && cp -R "$target/.apsy/analysis/." "$out/analysis/" 2>/dev/null || true
[[ -f "$target/.apsy/research-plan.md" ]] && cp "$target/.apsy/research-plan.md" "$out/preregistration/" || true
for f in decisions.md iteration-log.md deployment-log.md; do
  [[ -f "$target/.apsy/$f" ]] && cp "$target/.apsy/$f" "$out/docs/" || true
done

# --- Data: ANONYMIZED only ---
if [[ -n "$anon" && -d "$anon" ]]; then
  cp -R "$anon/." "$out/data/" && echo "• copied anonymized data from $anon"
else
  cat > "$out/data/README.md" <<'EOF'
# Data (anonymized only)
No anonymized data was bundled automatically. Add the PsyNet **anonymous/** export here
(`~/PsyNet-data/export/<app>/anonymous/data/`). NEVER add the non-anonymized (regular/) data to a
shared package — see config/ethics-policy.md §1.6.
EOF
fi

# --- Manifest ---
{
  echo "# Reproducibility package — $label"
  echo
  cat <<'EOF'
Generated by Auto-PsyNet (apsy).

- `code/` — the PsyNet experiment source (experiment.py, requirements.txt, config).
- `preregistration/` — the locked research plan (hypotheses, design, power, analysis plan).
- `analysis/` — analysis scripts + executed results (results.json) + figures.
- `data/` — anonymized data only.
- `docs/` — decisions/deviations + iteration + deployment logs.

To reproduce: install the pinned psynet (requirements.txt), run the experiment, export the data, then
run `analysis/analysis.py <data_dir>`.
EOF
} > "$out/README.md"

echo "✅ reproducibility package assembled at: $out"
ls -1 "$out"
