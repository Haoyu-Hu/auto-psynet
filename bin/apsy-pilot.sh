#!/usr/bin/env bash
# apsy-pilot — run the experiment with LLM-agent participants (gate G3) and export synthetic data.
# Stages the LLM-participant driver into the experiment dir (+ a conftest.py that enables it via the
# pytest harness psynet uses), sets APSY_LLM_PILOT=1, runs `psynet test local`, then `psynet export local`.
# Pilot N is the experiment's `test_n_bots` (the llm-pilot skill sets it). External-API backend only;
# the ambient-Claude backend is driven by the llm-pilot skill, not this wrapper.
# Usage: apsy-pilot.sh [experiment_dir]
set -uo pipefail
DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
. "$DIR/apsy-common.sh"; apsy_load_config
target="${1:-$PWD}"

if [[ "${APSY_LLM_PROVIDER:-}" == "ambient" || ( -z "${OPENAI_API_KEY:-}" && -z "${OPENROUTER_API_KEY:-}" ) ]]; then
  echo "ℹ️  No external LLM key (ambient mode). Ambient-Claude piloting is driven by the llm-pilot skill"
  echo "   (the orchestrator answers as the participant), not by this wrapper. Configure a key via"
  echo "   apsy:setup to use the scalable external-API pilot."
fi
if ! command -v psynet >/dev/null 2>&1; then
  echo "❌ psynet not installed — the G3 pilot needs the runtime. Run /apsy:doctor (install psynet or use EC2)."
  exit 3
fi
if [[ ! -f "$target/experiment.py" ]]; then
  echo "❌ no experiment.py in $target — build first (/apsy:build)."
  exit 1
fi

# Stage the driver + a conftest that enables it (pytest auto-loads conftest.py; `psynet test local` is pytest-based).
cp "$DIR/apsy_llm_participant.py" "$target/apsy_llm_participant.py"
cat > "$target/conftest.py" <<'PY'
# Auto-generated by apsy-pilot — enable the LLM-participant driver for this run.
# Harmless outside a pilot: the patch only routes to the LLM when APSY_LLM_PILOT=1.
import apsy_llm_participant
apsy_llm_participant.enable()
PY

echo "[apsy] G3 pilot → psynet test local with LLM participants (APSY_LLM_PILOT=1, provider=${APSY_LLM_PROVIDER:-openai})"
( cd "$target" && APSY_LLM_PILOT=1 psynet test local )
rc=$?
if [[ $rc -ne 0 ]]; then
  echo "❌ pilot run failed (rc=$rc) — inspect the experiment + logs. The design may be confusing the LLM,"
  echo "   or a page/control failed to render. This is exactly what G3 is meant to catch."
  exit $rc
fi
echo "[apsy] exporting synthetic pilot data → psynet export local"
( cd "$target" && psynet export local ) || echo "⚠️  export step failed; data may still be in the local db."
echo "✅ G3 pilot complete — synthetic (LLM) data exported. Next: run the analysis pipeline on it (/apsy:analyze)."
echo "   Reminder: this is SYNTHETIC data — never reported as human data (config/ethics-policy.md §2.4)."
